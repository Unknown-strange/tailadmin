  <script setup lang="ts">
  import { ref, onUnmounted } from 'vue'
  import { useRouter } from 'vue-router'
  import FullScreenLayout from '@/components/layout/FullScreenLayout.vue'
  import api from '@/services/api'
  
  const router = useRouter()
  
  // Form states
  const email = ref('')
  const otp = ref('')
  const newPassword = ref('')
  const confirmPassword = ref('')
  
  // UI states
  const currentStep = ref(1)
  const isLoading = ref(false)
  const errorMessage = ref('')
  const passwordError = ref('')
  const confirmPasswordError = ref('')
  const showNewPassword = ref(false)
  const showConfirmPassword = ref(false)
  const resendCountdown = ref(0)
  
  let resendTimer: ReturnType<typeof setInterval> | null = null
  
  // Step 1: Send verification code
  const handleSendCode = async () => {
    errorMessage.value = ''
    isLoading.value = true
  
    try {
      await api.post('/auth/forgot-password', {
        email: email.value
      })
      currentStep.value = 2
    } catch (error: any) {
      errorMessage.value = error.response?.data?.message || 'Failed to send verification code'
    } finally {
      isLoading.value = false
    }
  }
  
  // Step 2: Verify OTP
  const handleVerifyCode = async () => {
    errorMessage.value = ''
    isLoading.value = true
  
    try {
      await api.post('/auth/verify-code', {
        email: email.value,
        code: otp.value
      })
      currentStep.value = 3
    } catch (error: any) {
      errorMessage.value = error.response?.data?.message || 'Invalid verification code'
    } finally {
      isLoading.value = false
    }
  }
  
  // Resend code
  const handleResendCode = async () => {
    isLoading.value = true
    resendCountdown.value = 60
  
    if (resendTimer) clearInterval(resendTimer)
    resendTimer = setInterval(() => {
      resendCountdown.value--
      if (resendCountdown.value <= 0 && resendTimer) {
        clearInterval(resendTimer)
      }
    }, 1000)
  
    try {
      await api.post('/auth/forgot-password', {
        email: email.value
      })
      otp.value = ''
    } catch (error: any) {
      errorMessage.value = error.response?.data?.message || 'Failed to resend code'
    } finally {
      isLoading.value = false
    }
  }
  
  // Step 3: Reset password
  const handleResetPassword = async () => {
    errorMessage.value = ''
    passwordError.value = ''
    confirmPasswordError.value = ''
  
    // Validate
    if (newPassword.value.length < 8) {
      passwordError.value = 'Password must be at least 8 characters'
      return
    }
  
    if (newPassword.value !== confirmPassword.value) {
      confirmPasswordError.value = 'Passwords do not match'
      return
    }
  
    isLoading.value = true
  
    try {
      await api.post('/auth/reset-password', {
        email: email.value,
        code: otp.value,
        password: newPassword.value,
        password_confirmation: confirmPassword.value
      })
  
      // Redirect to signin after successful reset
      router.push('/signin')
    } catch (error: any) {
      errorMessage.value = error.response?.data?.message || 'Failed to reset password'
    } finally {
      isLoading.value = false
    }
  }
  
  // Cleanup on unmount
  onUnmounted(() => {
    if (resendTimer) clearInterval(resendTimer)
  })
  </script>